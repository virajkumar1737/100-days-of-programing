#include <stdio.h>

int main() {
    int n, i;

    printf("Enter a number: ");
    scanf("%d", &n);

    if (n <= 1) {
        printf("Not Prime");
        return 0;
    }

    for (i = 2; i * i <= n; i++) {
        if (n % i == 0) {
            printf("Not Prime");
            return 0;       // directly exit if divisible
        }
    }

    printf("Prime");
    return 0;
}
